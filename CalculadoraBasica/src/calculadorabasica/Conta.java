public class Conta {

    public static double somar(double n1, double n2) {
        return n1 + n2;
    }

    public static double diminuir(double n1, double n2) {
        return n1 - n2;
    }

    public static double dividir(double n1, double n2) {
        return n1 / n2;
    }

    public static double multiplicar(double n1, double n2) {
        return n1 * n2;
    }
}
